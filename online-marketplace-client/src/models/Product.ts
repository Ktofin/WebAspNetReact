/**
 * Интерфейс, описывающий товар в онлайн-магазине.
 */
export interface Product {
    /** Уникальный идентификатор товара */
    id: number;

    /** Название товара */
    name: string;

    /** Подробное описание товара */
    description: string;

    /** Цена товара в рублях */
    price: number;

    /** Базовое64-представление изображения товара (опционально) */
    imageData?: string;

    /** Идентификатор категории, к которой принадлежит товар */
    categoryId: number;

    /** Название категории */
    categoryName: string;

    /** Идентификатор продавца */
    sellerId: string;

    /** Имя пользователя продавца */
    sellerUsername: string;

    /** Флаг, указывающий на доступность товара для покупки */
    isAvailable: boolean;

    /** Дата создания товара в формате ISO (опционально) */
    creationDate?: string;
}
